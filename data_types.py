# Integer (int) data type
integer_number = 42
print(f"Integer Example: {integer_number}")

# Floating-Point (float) data type
floating_point_number = 3.14
print(f"Floating-Point Example: {floating_point_number}")

# String (str) data type
text = "Hello, Python!"
print(f"String Example: {text}")

# Boolean (bool) data type
is_python_fun = True
print(f"Boolean Example: {is_python_fun}")

# List data type
fruits = ["apple", "banana", "cherry"]
print(f"List Example: {fruits}")

# Tuple data type
coordinates = (3, 4)
print(f"Tuple Example: {coordinates}")

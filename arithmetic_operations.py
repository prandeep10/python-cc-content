# Take user input for values of 'a' and 'b'
a = float(input("Enter the first number (a): "))
b = float(input("Enter the second number (b): "))

# Addition
addition_result = a + b
print(f"{a} + {b} = {addition_result}")

# Subtraction
subtraction_result = a - b
print(f"{a} - {b} = {subtraction_result}")

# Multiplication
multiplication_result = a * b
print(f"{a} * {b} = {multiplication_result}")

# Division
division_result = a / b
print(f"{a} / {b} = {division_result}")

# Floor Division
floor_division_result = a // b
print(f"{a} // {b} = {floor_division_result}")

# Modulo
modulo_result = a % b
print(f"{a} % {b} = {modulo_result}")

# Exponentiation
exponentiation_result = a ** b
print(f"{a} ** {b} = {exponentiation_result}")
